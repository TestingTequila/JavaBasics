package _8methods;

public class Maths2 
{
	//Global Variable or Class level Variable 
	int a;
	int b;
	
	public void addition()
	{
		int sum =a+b;
		System.out.println(sum);
	}
	
	public void subtraction()
	{
		int diff =a-b;
		System.out.println(diff);
	}
	
	public void multiplication()
	{
		int product =a*b;
		System.out.println(product);
	}

}
