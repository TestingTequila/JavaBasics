package _6arrays;

public class ArraysPracticleExamples {

	public static void main(String[] args) {

		String[] tabs = {"Home", "Gift Certificates", "Brands", "Blogs", "Contact Us"};
		System.out.println("===============While loop===========================");
		
		int x = 0;
		while(x<tabs.length)
		{
			System.out.println(tabs[x]);
			x++;
		}
				
	}

}
