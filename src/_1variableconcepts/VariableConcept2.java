package _1variableconcepts;

public class VariableConcept2 {

	public static void main(String[] args) {
		
		//1. dataType nameOfVariable=value; [when we know the exact value at the time of creation]
		//2. dataType nameOfVariable;       [when we don't know the exact value at the time of creation]
		
		int x;
		x=10;
		
		double d;
        d=44.49;
        
        char c;
        c='d';
        
        System.out.println(x);
        System.out.println(d);
        System.out.println(c);
        
	}

}
