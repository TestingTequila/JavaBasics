package _1variableconcepts;

public class VariableConcepts {

	public static void main(String[] args) {
		
		// I want to do addition of two numbers 12 ,8 
		//How to create variables 
		//1. dataType nameOfVariable=value;
		int x = 12;
		double y=12.33;
		boolean z =true;
		
		System.out.println(x);
		System.out.println(y);
		System.out.println(z);

		
		//dataTypes: byte, short, int, long, float, double, char, boolean
		// In a class we cannot have two or more variables with the same name
		
		// Two types of Data Types
		   //1. Primitive Data Type [byte, short, int, long, float, double, char, boolean]
		          //We know in advance how much memory this will consume 
		          //We don't need objects in order to use them
		    
		   //2. Non-Primitive Data Type [String, Array, Class.....]
		         // We don't know in advance how much memory this will consume 
		         // We need objects in order to use them
	}

}
